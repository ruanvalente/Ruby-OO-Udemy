# Os operadores de incremento e decremento em Ruby são

numero = 2 

numero += 1 # É o mesmo que -> numero = numero + 1
numero -= 1 # É o mesmo que -> numero = numero - 1
numero *= 1 # É o mesmo que -> numero = numero * 1
numero /= 1 # É o mesmo que -> numero = numero / 1
numero %= 1 # É o mesmo que -> numero = numero % 1
numero **= 1 # É o mesmo que -> numero = numero ** 1